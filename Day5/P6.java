public class P6 {
    public static void main(String[] args) {    
       
        int number=0;
        //outer loop
        for(int i=1;i<5;i++){
            for(int j=1;j<=i;j++){
                System.out.print(number+" ");
                number++;
            }
            System.out.println();
        }

    }
}



public class TypeCasting {    
    public static void main(String[] args) {
        //byte-short-int-float-long-double
         //implicit type casting
        int a=10;
        float b=a;
        System.out.println(b);
       
        //explicit type casting
        float d=10.5f;
        int e=(int)d; 
        System.out.println(e);
        //char to int
        char c='A'; 
        int f=c;
        System.out.println(f);

       
    

    }
}

